public class CharVowelMethod {
    public void charCheckMethod(char letter) {
        if (letter == 'a' || letter == 'e' || letter == 'i' || letter == 'o' || letter == 'u') {
            System.out.println("The letter is vowel");
        } else {
            System.out.println("Letter is a consonant");
        }
    }
}
